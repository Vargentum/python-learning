tabby_cat = "\tI'm tabbed in." # make 'tabulation' at string start
persian_cat = "I'm split\non a line." # make a new line after 'split'
backslash_cat = "I'm \\ a \\ cat" # escape backslash characters

fat_cat = '''
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip\n\t* Grass
'''

print(tabby_cat)
print(persian_cat)
print(backslash_cat)
print(fat_cat)
